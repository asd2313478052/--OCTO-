package asd;

public class JIUJIU {
	public static void main(String[]args){
		int i = 1;
		String lang ="";
		while(i<10) {
			int j = 1
					
					;
			
			while(j<=i){
				lang += i +"x"+j+"="+i*j+" ";
				j++;
				
			}
			System.out.println(lang);
			lang="";
			i++;
		}
	}

}
